/*将1、2、...、9其9个数分成三组，分别组成3个三位数，
使这3个三位数构成1:2:3 的比例，试求出所有满足条件的3个三位数。
例如，3个三位数192、384、576满足以上条件。*/
#include <stdio.h>

int main() {
    int i, j, k, a, b, c;
    int count = 0; // 用于计数满足条件的组合数量

    // 外层循环代表第一个三位数的百位
    for (i = 1; i <= 9; i++) {
        // 中层循环代表第二个三位数的百位
        for (j = 1; j <= 9; j++) {
            // 内层循环代表第三个三位数的百位
            for (k = 1; k <= 9; k++) {
                if (i != j && j != k && k != i) { // 确保三个数的百位不同
                    a = i * 100 + (j % 4) * 10 + (j / 4); // 第一个三位数
                    b = j * 100 + (k % 4) * 10 + (k / 4); // 第二个三位数
                    c = k * 100 + (i % 4) * 10 + (i / 4); // 第三个三位数

                    // 检查是否满足1:2:3的比例
                    if (a * 2 == b && b * 3 == 3 * c) {
                        printf("%d, %d, %d\n", a, b, c);
                        count++;
                    }
                }
            }
        }
    }

    if (count == 0) {
        printf("No solutions found.\n");
    }

    return 0;
}
